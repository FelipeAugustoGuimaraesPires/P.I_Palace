    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(tblProduto.getSelectedRow()<0){
                lblMensagemBusca.setText("Por favor selecione um cliente");
        }else{
            int linhaSelecionada = tblProduto.getSelectedRow();

            String id = tblProduto.getValueAt(linhaSelecionada, 0).toString();
            int ID =Integer.parseInt(id);

            ArrayList<Produto> lista = dao.ProdutoDAO.ListaAlterar(ID);

            for(Produto item : lista){
                txtNomeProdutoAtualiza.setText(item.getNome());
                txtDescricaoProdutoAtualiza.setText(item.getDescricao());
                txtQuantidadeAtualiza.setText(String.valueOf(item.getQuantidade()));
                txtValorProdutoAtualiza.setText(String.valueOf(item.getValor()));

            lblId.setText(id);
            pnlGuias.setSelectedIndex(2);
            }    
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nome=txtNomeBusca.getText();
        if(txtIdBusca.getText().length()==0 && txtValorMinBusca.getText().length()==0 && txtValorMaxBusca.getText().length()==0){
            ArrayList<Produto> lista = dao.ProdutoDAO.Listar(nome);

            DefaultTableModel modelo = (DefaultTableModel)tblProduto.getModel();
            modelo.setRowCount(0);
            for(Produto item : lista){
                modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getDescricao()), String.valueOf(item.getValor())});
            }
        }else{
            double valormax=Double.parseDouble(txtValorMaxBusca.getText());
            double valormin = Double.parseDouble(txtValorMinBusca.getText());
            int id=Integer.parseInt(txtIdBusca.getText());
            
            
            if(valormax!=0 && valormin!=0 && id!=0){
                ArrayList<Produto> lista = dao.ProdutoDAO.Listar(id, nome, valormax, valormin);

                DefaultTableModel modelo = (DefaultTableModel)tblProduto.getModel();
                modelo.setRowCount(0);
                for(Produto item : lista){
                    modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getDescricao()), String.valueOf(item.getValor())});
                }
            }else{
                if(valormax!=0 && valormin==0 && id==0){
                    ArrayList<Produto> lista = dao.ProdutoDAO.ListarValorMin(nome, valormin);

                    DefaultTableModel modelo = (DefaultTableModel)tblProduto.getModel();
                    modelo.setRowCount(0);
                    for(Produto item : lista){
                        modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getDescricao()), String.valueOf(item.getValor())});
                    }
                }else{
                    if(valormax==0 && valormin!=0 && id==0){
                        ArrayList<Produto> lista = dao.ProdutoDAO.ListarValorMax(nome, valormax);

                        DefaultTableModel modelo = (DefaultTableModel)tblProduto.getModel();
                        modelo.setRowCount(0);
                        for(Produto item : lista){
                            modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getDescricao()), String.valueOf(item.getValor())});
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

