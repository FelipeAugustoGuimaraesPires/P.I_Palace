    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(txtDescricaoProduto.getText().length() == 0 || txtNomeProduto.getText().length() == 0 || txtValorProduto.getText().length() == 0 || txtQuantidade.getText().length() == 0){
            
            if(txtNomeProduto.getText().length()==0){
                txtNomeProduto.setBackground(Color.RED);
            }
            
            if(txtDescricaoProduto.getText().length()==0){
                txtDescricaoProduto.setBackground(Color.RED);
            }
            
            if(txtValorProduto.getText().length()==0){
                txtValorProduto.setBackground(Color.RED);
            }
            
            if(txtQuantidade.getText().length()==0){
                txtQuantidade.setBackground(Color.RED);
            }
            
            lblMensagem.setForeground(Color.red);
            lblMensagem.setText("Por favor, preencha todos os campos");
            
        }else{
            lblMensagem.setText(" ");
            
            String nome = txtNomeProduto.getText();
            String descricao = txtDescricaoProduto.getText();
            double valor =Double.parseDouble(txtValorProduto.getText());
            int quantidade =Integer.parseInt(txtQuantidade.getText());
            
            Produto obj = new Produto (nome, descricao, valor, quantidade);
            
            boolean salvo= ProdutoDAO.Salvar(obj);
            if(salvo){
            JOptionPane.showMessageDialog(this, "Produto adicionado com sucesso");
            }else{
            JOptionPane.showMessageDialog(rootPane, "Falha ao adicionar produto!");
            
            
            txtDescricaoProduto.setText("");
            txtNomeProduto.setText("");
            txtValorProduto.setText("");
            txtQuantidade.setText("");
        }
            }
    }//GEN-LAST:event_btnConfirmarActionPerformed

