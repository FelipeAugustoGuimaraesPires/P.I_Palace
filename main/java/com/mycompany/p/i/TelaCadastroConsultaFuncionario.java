    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        String nome=txtNome.getText();
        String cpf=txtCPF.getText();
        Date nascimento=jdcData.getDate();
        String sexo = cbSexo.getSelectedItem().toString();
        String estado=txtEstado.getText();
        String telefone=txtTelefone.getText();
        String login=txtLogin.getText();
        String senha=txtSenha.getText();

        if(nome.length()==0 || txtCPF.getText().length()!=11 || jdcData.getDate()==null || sexo.equals("Selecione") || txtLogin.getText().length()==0 || txtSenha.getText().length()<8){
            lblMensagem.setForeground(Color.red);
            lblMensagem.setText("Por favor, preencha todos os campos mínimos necessários");

            if(txtNome.getText().length()==0){
                txtNome.setBackground(Color.RED);
            }

            if(txtCPF.getText().length()!=11){
                txtCPF.setBackground(Color.RED);
            }

            if(jdcData.getDate()==null){
                jdcData.setBackground(Color.RED);
            }

            if(sexo.equals("Selecione")){
                cbSexo.setBackground(Color.red);
            }
            
            if(txtLogin.getText().length()==0){
                txtLogin.setBackground(Color.red);
            }
            
            if(txtSenha.getText().length()<8){
                txtSenha.setBackground(Color.red);
                txtSenha.setText("Mínimo de 8 caractéres");
            }

        }else{
            jdcData.setBackground(Color.WHITE);
            lblMensagem.setForeground(Color.BLACK);
            lblMensagem.setText(" ");

            Funcionario obj = new Funcionario (nome, sexo, estado, nascimento, telefone, cpf);
            
            boolean salvo= dao.FuncionarioDAO.salvar(obj);
            if(salvo){
                JOptionPane.showMessageDialog(this, "Funcionario adicionado com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }
            
            Funcionario id = dao.FuncionarioDAO.pegaId(cpf);
            
            int idFuncionario = (id.getID());
            
            Login objLogin = new Login(login, senha, idFuncionario);
            
            boolean salvologin= dao.LoginDAO.salvar(objLogin, idFuncionario);
            if(salvo){
                JOptionPane.showMessageDialog(this, "Login adicionado com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }

            txtCPF.setText("");
            txtNome.setText("");
            txtEstado.setText("");
            txtNome.setText("");
            txtTelefone.setText("");
            txtLogin.setText("");
            txtSenha.setText("");
            cbSexo.setSelectedIndex(0);
            jdcData.setCalendar(null);

        }
            
    }//GEN-LAST:event_btnAdicionarActionPerformed

