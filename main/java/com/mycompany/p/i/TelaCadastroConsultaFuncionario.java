    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tblConsulta.getSelectedRow()<0){
            JOptionPane.showMessageDialog(rootPane, "Por favor selecione um funcionario");        }else{
            int linhaSelecionada = tblConsulta.getSelectedRow();

            String id = tblConsulta.getValueAt(linhaSelecionada, 0).toString();
            int ID =Integer.parseInt(id);

            ArrayList<Funcionario> lista = dao.FuncionarioDAO.ListaAlterar(ID);

            for(Funcionario item : lista){
                txtNomeEditar.setText(item.getNome());
                txtCPFEditar.setText(item.getCpf());
                //sexo
                txtEstadoEditar.setText(item.getEstado());
                txtTelefoneEditar.setText(item.getTelefone());
                //EstadoCivil
                jdcDataEditar.setDate(item.getDataNascimento());
            }

            lblId.setText(id);
            pnlGuias.setSelectedIndex(2);
       }
    }//GEN-LAST:event_btnEditarActionPerformed

