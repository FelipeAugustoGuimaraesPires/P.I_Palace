    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String nome=txtNomeEditar.getText();
        String cpf=txtCPFEditar.getText();
        Date nascimento=jdcDataEditar.getDate();
        String sexo = cbSexoEditar.getSelectedItem().toString();
        String estado=txtEstadoEditar.getText();
        String telefone=txtTelefoneEditar.getText();
        String login=txtLoginEditar.getText();
        String senha=txtSenhaEditar.getText();

        if(nome.length()==0 || txtCPFEditar.getText().length()!=11 || jdcDataEditar.getDate()==null || sexo.equals("Selecione") || txtLoginEditar.getText().length()==0 || txtSenhaEditar.getText().length()<8){

            if(txtNomeEditar.getText().length()==0){
                txtNomeEditar.setBackground(Color.RED);
            }

            if(txtCPFEditar.getText().length()!=11){
                txtCPFEditar.setBackground(Color.RED);
            }

            if(jdcDataEditar.getDate()==null){
                jdcDataEditar.setBackground(Color.RED);
            }

            if(sexo.equals("Selecione")){
                cbSexoEditar.setBackground(Color.red);
            }
            
            if(txtLoginEditar.getText().length()==0){
                txtLoginEditar.setBackground(Color.red);
            }
            
            if(txtSenhaEditar.getText().length()<8){
                txtSenhaEditar.setBackground(Color.red);
                txtSenhaEditar.setText("Mínimo de 8 caractéres");
            }

        }else{
            jdcDataEditar.setBackground(Color.WHITE);

            Funcionario obj = new Funcionario (nome, sexo, estado, nascimento, telefone, cpf);
            int id = Integer.parseInt(lblId.getText());

            //Login obj = new Login(login, senha, id);
            
            boolean salvo= dao.FuncionarioDAO.Atualizar(id, obj);
            if(salvo){
                JOptionPane.showMessageDialog(this, "Funcionario editado com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha!");
            }
            
            

            txtCPFEditar.setText("");
            txtNomeEditar.setText("");
            txtEstadoEditar.setText("");
            txtNomeEditar.setText("");
            txtTelefone.setText("");
            txtLoginEditar.setText("");
            txtSenhaEditar.setText("");
            cbSexoEditar.setSelectedIndex(0);
            jdcDataEditar.setCalendar(null);
            
            lblId.setText("");
            

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

