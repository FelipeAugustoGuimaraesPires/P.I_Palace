    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        
        String nome=txtNome.getText();
        String cpf=txtCPF.getText();
        String email=txtEmail.getText();
        Date nascimento=jdcData.getDate();
        String sexo = cbSexo.getSelectedItem().toString();
        String estado=txtEstado.getText();
        String cidade=txtCidade.getText();
        String telefone=txtTelefone.getText();
        String estadoCivil =cbEstadoCivil.getSelectedItem().toString();
      
        if(nome.length()==0 || txtCPF.getText().length()!=11 || email.length()==0 || jdcData.getDate()==null || sexo.equals("Selecione") || estadoCivil.equals("Selecione")){
            lblMensagem.setForeground(Color.red);
            lblMensagem.setText("Por favor, preencha todos os campos mínimos necessários");
           
            if(txtNome.getText().length()==0){
                txtNome.setBackground(Color.RED);
            }
            
            if(txtCPF.getText().length()!=11){
                txtCPF.setBackground(Color.RED);
            }
            
            if(txtEmail.getText().length()==0){
                txtEmail.setBackground(Color.RED);
            }
            
            if(jdcData.getDate()==null){
                jdcData.setBackground(Color.RED);
            }
            
            if(sexo.equals("Selecione")){
                cbSexo.setBackground(Color.red);
            }
            
            if(estadoCivil.equals("Selecione")){
                cbEstadoCivil.setBackground(Color.red);
            }
            
        }else{
            jdcData.setBackground(Color.WHITE);
            lblMensagem.setForeground(Color.BLACK);
            lblMensagem.setText(" ");
            
            Cliente obj = new Cliente (nome, email, sexo, estado, cidade, estadoCivil, nascimento, telefone, cpf);
            
           
            boolean salvo= ClienteDAO.salvar(obj);
            if(salvo){
            JOptionPane.showMessageDialog(this, "Cliente adicionado com sucesso");
            }else{
            JOptionPane.showMessageDialog(rootPane, "Falha!");
        }
            
            
            txtCPF.setText("");
            txtNome.setText("");
            txtCidade.setText("");
            txtEmail.setText("");
            txtEstado.setText("");
            txtNome.setText("");
            txtTelefone.setText("");
            cbSexo.setSelectedIndex(0);
            cbEstadoCivil.setSelectedIndex(0);
            jdcData.setCalendar(null);
            
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

