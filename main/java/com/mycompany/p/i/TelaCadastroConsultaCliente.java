    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed

        int id=Integer.parseInt(lblId.getText());
        
        String nome=txtNomeAtualiza.getText();
        String cpf=txtCPFAtualiza.getText();
        String email=txtEmailAtualiza.getText();
        Date nascimento=jdcDataAtualiza.getDate();
        String sexo = cbSexoAtualiza.getSelectedItem().toString();
        String estado=txtEstadoAtualiza.getText();
        String cidade=txtCidadeAtualiza.getText();
        String telefone=txtTelefoneAtualiza.getText();
        String estadoCivil =cbEstadoCivilAtualiza.getSelectedItem().toString();
        
        if(id!=0){
            if(nome.length()==0 || txtCPFAtualiza.getText().length()!=11 || email.length()==0 || jdcDataAtualiza.getDate()==null || sexo.equals("Selecione") || estadoCivil.equals("Selecione")){
            lblErroAtualiza.setForeground(Color.red);
            lblErroAtualiza.setText("Por favor, preencha todos os campos mínimos necessários");
           
            if(txtNomeAtualiza.getText().length()==0){
                txtNomeAtualiza.setBackground(Color.RED);
            }
            
            if(txtCPFAtualiza.getText().length()!=11){
                txtCPFAtualiza.setBackground(Color.RED);
            }
            
            if(txtEmailAtualiza.getText().length()==0){
                txtEmailAtualiza.setBackground(Color.RED);
            }
            
            if(jdcDataAtualiza.getDate()==null){
                jdcDataAtualiza.setBackground(Color.RED);
            }
            
            if(sexo.equals("Selecione")){
                cbSexoAtualiza.setBackground(Color.red);
            }
            
            if(estadoCivil.equals("Selecione")){
                cbEstadoCivilAtualiza.setBackground(Color.red);
            }
            
            }else{
                lblErroAtualiza.setText("");

                Cliente obj = new Cliente (nome, email, sexo, estado, cidade, estadoCivil, nascimento, telefone, cpf);

                boolean retorno = dao.ClienteDAO.atualizar(obj, id);   
                if(retorno){
                    JOptionPane.showMessageDialog(rootPane, "Atualizada com sucesso");
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Falha ao atualizar");
                }
            }
        }else{
            lblErroAtualiza.setForeground(Color.red);
            lblErroAtualiza.setText("Selecione um cliente para atualizar");
        }
        
    }//GEN-LAST:event_AtualizarActionPerformed

