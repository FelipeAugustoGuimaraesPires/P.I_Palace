    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if(tblConsultaCliente.getSelectedRow()<0){
                lblMensagemBusca.setText("Por favor selecione um cliente");
        }else{
            int linhaSelecionada = tblConsultaCliente.getSelectedRow();

            String id = tblConsultaCliente.getValueAt(linhaSelecionada, 0).toString();
            int ID =Integer.parseInt(id);

            ArrayList<Cliente> lista = dao.ClienteDAO.ListaAlterar(ID);

            for(Cliente item : lista){
                txtNomeAtualiza.setText(item.getNome());
                txtCPFAtualiza.setText(item.getCpf());
                txtEmailAtualiza.setText(item.getEmail());
                //sexo
                
                txtEstadoAtualiza.setText(item.getEstado());
                txtCidadeAtualiza.setText(item.getCidade());
                txtTelefoneAtualiza.setText(item.getTelefone());
                //EstadoCivil
                jdcDataAtualiza.setDate(item.getDataNascimento());
            }

            lblId.setText(id);
            pnlGuias.setSelectedIndex(2);
       }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

