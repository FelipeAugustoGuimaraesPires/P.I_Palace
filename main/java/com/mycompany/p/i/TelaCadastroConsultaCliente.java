    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String cpf = txtBuscaCPF.getText();
        String nome = txtBuscaNome.getText();
        
        if(cpf.isBlank()==true&&nome.isBlank()==true){
            ArrayList<Cliente> lista = dao.ClienteDAO.Listar();

            DefaultTableModel modelo = (DefaultTableModel)tblConsultaCliente.getModel();
            modelo.setRowCount(0);
            for(Cliente item : lista){
                modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getCpf()), String.valueOf(item.getEmail()), String.valueOf(item.getTelefone()) });
            }
        }else{
            if(cpf.isBlank()==true&&nome.isBlank()==false){
                ArrayList<Cliente> lista = dao.ClienteDAO.Listar(nome);

                DefaultTableModel modelo = (DefaultTableModel)tblConsultaCliente.getModel();
                modelo.setRowCount(0);
                for(Cliente item : lista){
                    modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getCpf()), String.valueOf(item.getEmail()), String.valueOf(item.getTelefone()) });
                }
            }else{
                if(cpf.isBlank()==false&&nome.isBlank()==true){
                    ArrayList<Cliente> lista = dao.ClienteDAO.ListarCpf(cpf);

                    DefaultTableModel modelo = (DefaultTableModel)tblConsultaCliente.getModel();
                    modelo.setRowCount(0);
                    for(Cliente item : lista){
                        modelo.addRow(new String[]{String.valueOf(item.getId()), String.valueOf(item.getNome()), String.valueOf(item.getCpf()), String.valueOf(item.getEmail()), String.valueOf(item.getTelefone()) });
                    }
                }
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

