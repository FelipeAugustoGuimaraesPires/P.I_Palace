    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(tblConsultaCliente.getSelectedRow()<0){
            lblMensagemBusca.setText("Por favor selecione um cliente");
        }else{
            int linhaSelecionada = tblConsultaCliente.getSelectedRow();
            DefaultTableModel modelo = (DefaultTableModel)tblConsultaCliente.getModel();

            int id = Integer.parseInt(tblConsultaCliente.getValueAt(linhaSelecionada, 0).toString());
            boolean retorno = dao.ClienteDAO.excluir(id);   
            if(retorno){
                modelo.removeRow(linhaSelecionada);
                JOptionPane.showMessageDialog(rootPane, "Excluída com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Falha ao excluír");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

